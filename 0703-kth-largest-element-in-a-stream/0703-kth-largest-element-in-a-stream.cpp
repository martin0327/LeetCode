class KthLargest {
public:
    int k;
    priority_queue<int, vector<int>, greater<int>> pq;
    KthLargest(int k, vector<int>& a) {
        this->k = k;
        for (auto x : a) pq.push(x);
    }
    
    int add(int x) {
        pq.push(x);
        while (pq.size() > k) pq.pop();
        return pq.top();
    }
};

