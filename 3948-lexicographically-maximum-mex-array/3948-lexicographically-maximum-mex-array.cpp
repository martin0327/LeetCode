const int inf = 2e9;
using S = int;
S op(S x, S y) { return min(x,y); }
S e() { return inf; }

class Solution {
public:
    vector<int> maximumMEX(vector<int>& a) {
        int n = a.size(), sz = 1e5+1;
        segtree<S,op,e> seg(vi(sz,0));
        for (auto x : a) {
            seg.set(x, seg.get(x)+1);
        }

        auto f = [&] () {
            int ret = seg.max_right(0, [&] (int x) {
                return x > 0;
            });
            return ret;
        };

        vi ans;
        reverse(a.begin(), a.end());
        int cnt = 0;
        while (a.size()) {
            auto mex = f();
            ans.push_back(mex);
            if (mex == 0) {
                auto x = a.back();
                a.pop_back();
            }
            else {
                int n = a.size(), idx = -1;
                set<int> s;
                for (int i=n-1; i>=0; i--) {
                    if (a[i] >= mex) continue;
                    else s.insert(a[i]);
                    if (s.size() == mex) {
                        idx = i;
                        break;
                    }
                }
                while (a.size() > idx) {
                    auto x = a.back();
                    seg.set(x, seg.get(x)-1);
                    a.pop_back();
                }
            }
        }
        return ans;
    }
};